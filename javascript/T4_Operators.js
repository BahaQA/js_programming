
//Aritmetic, short-hand, and logical operators are the same.


/**
 * All relational operators return boolean
 * Relational operators different than Java: 
 * == Equal
 * === Strict equal
 *q  != Not equal
 !== strict not equal
 */

 console.log('100' == 100); //true. Compares only values, ignores types. Not safe to use.
 console.log('100' === 100); // false. Values and the types must be the same. Use this. 

 console.log(100 != '100'); //false. Compares only values
 console.log(100 !== '100'); //true. Values and the types must be the same. Use this. 
